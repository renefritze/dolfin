#include <dolfin/basic.h>

using namespace dolfin;

//-----------------------------------------------------------------------------
real dolfin::sqr(real x)
{
  return x*x;
}
//-----------------------------------------------------------------------------
real dolfin::max(real x, real y)
{
  return ( x > y ? x : y );
}
//-----------------------------------------------------------------------------
real dolfin::min(real x, real y)
{
  return ( x < y ? x : y );
}
//-----------------------------------------------------------------------------
